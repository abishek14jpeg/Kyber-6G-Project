import os
import glob
import math
import numpy as np
import pandas as pd
import matplotlib.pyplot as plt

def load_data(files):
    """Loads a list of CSV files returning a combined dataframe with metadata."""
    df_list = []
    for file in files:
        if not os.path.exists(file):
            continue
        try:
            # Filename convention: drone_swarm_metrics_{crypto}_{nodes}.csv
            parts = os.path.basename(file).split('.')[0].split('_')
            crypto = parts[3]
            nodes = int(parts[4])
            
            df = pd.read_csv(file)
            df['Crypto'] = crypto
            df['Nodes'] = nodes
            df_list.append(df)
        except Exception as e:
            print(f"Error reading {file}: {e}")
            
    if df_list:
        return pd.concat(df_list, ignore_index=True)
    return pd.DataFrame()

